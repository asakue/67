CREATE VIEW dbo.v_ОтделКадров AS
SELECT
    s.[ФИО],
    s.[Возраст],
    s.[Пол],
    s.[Адрес],
    s.[Телефон],
    s.[Паспортные данные],
    d.[Наименование должности] AS [Должность],
    d.[Оклад],
    z.[Наименование]           AS [Звание],
    z.[Надбавка],
    d.[Оклад] + z.[Надбавка]  AS [Итого зарплата]
FROM dbo.Сотрудники AS s
JOIN dbo.Должности       AS d ON d.[Код должности] = s.[Код должности]
JOIN dbo.Звания          AS z ON z.[Код звания]    = s.[Код звания];
GO

CREATE VIEW dbo.v_Преступники AS
SELECT
    c.[Номер дела],
    c.[ФИО]              AS [ФИО преступника],
    c.[Дата рождения],
    c.[Пол],
    c.[Адрес],
    vc.[Наименование]    AS [Вид преступления],
    vc.[Статья],
    vc.[Наказание],
    vc.[Срок],
    p.[ФИО]              AS [Пострадавший],
    c.[Состояние],
    s.[ФИО]              AS [Сотрудник]
FROM dbo.Преступники           AS c
JOIN dbo.ВидыПреступлений      AS vc ON vc.[Код вида преступления] = c.[Код вида преступления]
JOIN dbo.Пострадавшие          AS p  ON p.[Код пострадавшего]      = c.[Код пострадавшего]
JOIN dbo.Сотрудники            AS s  ON s.[Код сотрудника]         = c.[Код сотрудника];
GO

