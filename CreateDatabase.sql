CREATE TABLE dbo.Должности
(
    [Код должности]          int IDENTITY(1,1) NOT NULL CONSTRAINT PK_Должности PRIMARY KEY,
    [Наименование должности] nvarchar(80)      NOT NULL,
    [Оклад]                  decimal(12,2)     NOT NULL,
    [Обязанности]            nvarchar(300)     NOT NULL,
    [Требования]             nvarchar(300)     NOT NULL
);

CREATE TABLE dbo.Звания
(
    [Код звания]   int IDENTITY(1,1) NOT NULL CONSTRAINT PK_Звания PRIMARY KEY,
    [Наименование] nvarchar(80)      NOT NULL,
    [Надбавка]     decimal(12,2)     NOT NULL,
    [Обязанности]  nvarchar(300)     NOT NULL,
    [Требования]   nvarchar(300)     NOT NULL
);

CREATE TABLE dbo.ВидыПреступлений
(
    [Код вида преступления] int IDENTITY(1,1) NOT NULL CONSTRAINT PK_ВидыПреступлений PRIMARY KEY,
    [Наименование]          nvarchar(120)     NOT NULL,
    [Статья]                nvarchar(30)      NOT NULL,
    [Наказание]             nvarchar(220)     NOT NULL,
    [Срок]                  int               NOT NULL
);

CREATE TABLE dbo.Пострадавшие
(
    [Код пострадавшего] int IDENTITY(1,1) NOT NULL CONSTRAINT PK_Пострадавшие PRIMARY KEY,
    [ФИО]               nvarchar(120)     NOT NULL,
    [Дата рождения]     date              NOT NULL,
    [Пол]               nvarchar(10)      NOT NULL,
    [Адрес]             nvarchar(180)     NOT NULL
);

CREATE TABLE dbo.Сотрудники
(
    [Код сотрудника]    int IDENTITY(1,1) NOT NULL CONSTRAINT PK_Сотрудники PRIMARY KEY,
    [ФИО]               nvarchar(120)     NOT NULL,
    [Возраст]           int               NOT NULL,
    [Пол]               nvarchar(10)      NOT NULL,
    [Адрес]             nvarchar(180)     NOT NULL,
    [Телефон]           nvarchar(30)      NOT NULL,
    [Паспортные данные] nvarchar(80)      NOT NULL,
    [Код должности]     int               NOT NULL,
    [Код звания]        int               NOT NULL,
    CONSTRAINT FK_Сотрудники_Должности FOREIGN KEY ([Код должности]) REFERENCES dbo.Должности([Код должности]),
    CONSTRAINT FK_Сотрудники_Звания    FOREIGN KEY ([Код звания])    REFERENCES dbo.Звания([Код звания])
);

CREATE TABLE dbo.Преступники
(
    [Номер дела]            int IDENTITY(1001,1) NOT NULL CONSTRAINT PK_Преступники PRIMARY KEY,
    [ФИО]                   nvarchar(120)        NOT NULL,
    [Дата рождения]         date                 NOT NULL,
    [Пол]                   nvarchar(10)         NOT NULL,
    [Адрес]                 nvarchar(180)        NOT NULL,
    [Код вида преступления] int                  NOT NULL,
    [Код пострадавшего]     int                  NOT NULL,
    [Состояние]             nvarchar(60)         NOT NULL,
    [Код сотрудника]        int                  NOT NULL,
    CONSTRAINT FK_Преступники_Виды        FOREIGN KEY ([Код вида преступления]) REFERENCES dbo.ВидыПреступлений([Код вида преступления]),
    CONSTRAINT FK_Преступники_Пострадавшие FOREIGN KEY ([Код пострадавшего])     REFERENCES dbo.Пострадавшие([Код пострадавшего]),
    CONSTRAINT FK_Преступники_Сотрудники  FOREIGN KEY ([Код сотрудника])        REFERENCES dbo.Сотрудники([Код сотрудника])
);
GO

